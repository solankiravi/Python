firstNum = thirdNum = 1 
secondNum = fourthNum = 0

#Bitwise And : It is like multiplication
print("Bitwise And")
print(firstNum & secondNum)
print(firstNum & thirdNum)

#Bitwise or :  It is like Addition
print("Bitwise or ")
print(firstNum | secondNum)

#Bitwise XOR : return true only when exactly one is true
print("Bitwise XOR")
print(firstNum ^ secondNum)
print(firstNum ^ thirdNum)

#Compliment operator : takes number’s binary, and returns its one’s complement.
print("Compliment operator")
print(~firstNum)