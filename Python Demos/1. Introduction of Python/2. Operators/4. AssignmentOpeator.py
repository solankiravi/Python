firstNum = 3;

tempVal = firstNum;
#Assignment
print("First Number is assigned with value ",tempVal)

#Assignment with Arithmetic operators

tempVal += firstNum   # tempVal += firstNum = tempVal = tempVal + firstNum
print(tempVal)

tempVal -= firstNum
print(tempVal)

tempVal *= firstNum
print(tempVal)

tempVal /= firstNum
print(tempVal)