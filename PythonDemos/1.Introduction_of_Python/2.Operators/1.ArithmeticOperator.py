firstNum = 3;
secondNum = 5;

#Addition
print("Addition :",firstNum + secondNum)

#Substraction
print("Substraction",firstNum - secondNum)

#Multiply
print("Multiply",firstNum * secondNum)

#Division
print("Division",firstNum / secondNum)

#Floor Division
print("Floor Division",firstNum // secondNum)

#Remainder
print("Remainder",firstNum % secondNum)

#Power
print("Power",firstNum ** secondNum)